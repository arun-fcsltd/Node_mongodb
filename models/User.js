const mongoose = require("mongoose");
const bcrypt = require("bcryptjs");

const userSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phone: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
      enum: ["admin", "vendor", "worker", "store-admin", "scrap-admin"],
      default: "worker",
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

// Hash password before saving
userSchema.pre("save", async function (next) {
  if (!this.isModified("password")) return next(); // ‚úÖ Skip if already hashed or not changed

  this.password = await bcrypt.hash(this.password, 10); // üîê Hash with salt rounds = 10
  next();
});

module.exports = mongoose.model("User ", userSchema);
